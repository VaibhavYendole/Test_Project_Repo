package loops;

public class ForAscendingOrder 
{
	public static void main(String[] args) 
	{		
		for (int i=1; i<=10; i++) 		// i++ or i+1 is Increament by 1
										// i+2 --> Increament by 2
		{
			System.out.println(i);		// i--  = Decreament by 1
			
		}								// i-2	= Decreament by 2							
		
	}										
}										
