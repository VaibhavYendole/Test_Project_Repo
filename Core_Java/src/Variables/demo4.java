package Variables;

public class demo4 {
	
	public static void main(String[] args) {
		
		String Name;
		float height;
		int Age;
		char Type;
		
		Name = "Vaibhav";
		height = 5.6f;
		Age = 28;
		Type = 'A';
		
		
		// + sign indicates for Concat
		System.out.println("Name : "+Name);
		System.out.println("Height : "+height+" (In feet)");
		System.out.println("Age : "+Age+" (In Years)");
		System.out.println(Type);
				
	
	}

}
