package Star_Examples;

public class Star_Box3 
{
	//*************			Rows = 4
	//*************			Columns = 13
	//*************
	//*************
	public static void main(String[] args)
	{
		
	for (int i =1; i<=4; i++)
	{
		for (int j=1; j<=13; j++)
		{
			System.out.print("*");
		}
		System.out.println();
	}
	}
}
	
