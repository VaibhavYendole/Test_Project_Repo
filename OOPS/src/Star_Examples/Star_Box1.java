package Star_Examples;

public class Star_Box1 
{
	//*********			Rows = 05
	//*********			Columns = 09
	//*********
	//*********
	//*********

public static void main(String[] args)

{
	for (int i=1; i<=5; i++)
	{
		for (int j=1; j<=9; j++)
		{
			System.out.print("*");
		}
		System.out.println();
	}		
}
}

